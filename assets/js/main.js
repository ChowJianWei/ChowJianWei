const formatTimeRange = (startISO, endISO) => {
  const startDate = new Date(startISO);
  const endDate = new Date(endISO);
  const formatter = new Intl.DateTimeFormat('en-US', {
    hour: 'numeric',
    minute: '2-digit',
    timeZoneName: 'short'
  });
  const dayFormatter = new Intl.DateTimeFormat('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric'
  });
  if (startDate.getTime() === endDate.getTime()) {
    return `${dayFormatter.format(startDate)} ¬∑ ${formatter.format(startDate)}`;
  }
  return `${dayFormatter.format(startDate)} ¬∑ ${formatter.format(startDate)} ‚Äì ${formatter.format(endDate)}`;
};

